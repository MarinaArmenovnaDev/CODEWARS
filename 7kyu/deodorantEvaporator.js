function evaporator(content, evap_per_day, threshold){ 
    let dayCount = 0
   let porog = content * (threshold / 100)
    while(content > porog){ 
       content -= content * (evap_per_day / 100)
        dayCount += 1
       
    }
    return dayCount
  }

  console.log(evaporator(10, 10, 5));
